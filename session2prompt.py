# sum of two floating point numbers; print data type of resulting answer
a = 2.5
b = 7.5

print("a + b =", a+b)
c = a+b
print("data type of result:", type(c))

# the difference between two integers; print data type of resulting answer
x = 4
y = 6

print("x - y =", x-y)
z = x-y
